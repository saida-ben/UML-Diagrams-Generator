package org.mql.java.reflection.models;

public class AnnotationInfo {
	private String name;
	
	public AnnotationInfo() {}
	

	public AnnotationInfo(String name) {
		super();
		this.name = name;
	}


	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}

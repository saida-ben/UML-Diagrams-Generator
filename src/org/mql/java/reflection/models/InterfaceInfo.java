package org.mql.java.reflection.models;

public class InterfaceInfo {
	private String name;
	
	public InterfaceInfo() {}

	public InterfaceInfo(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}


}
